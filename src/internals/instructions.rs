fn parse_instruction(i: u16) {
    match i {
        0x00E0 => (), // Clear Display
        0x00EE => (), // Return from a subroutine.
        0x1000..0x2000 => (),
        0x2000..0x3000 => (),
        0x3000..0x4000 => (),
        0x4000..0x5000 => (),
        0x5000..0x6000 => (),
        0x6000..0x7000 => (),
        0x7000..0x8000 => (),
        0x8000..0x9000 => (),
        0x9000..0xA000 => (),
        0xA000..0xB000 => (),
        0xB000..0xC000 => (),
        0xC000..0xD000 => (),
        0xD000..0xE000 => (),
        0xE000..0xF000 => (),
        0xF000..=0xFFFF => (),
        _ => (),
    }
}
